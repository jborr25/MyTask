package Task8ForLoop;

public class ForLoop {
    public static void main(String[] args) {
        
        for (int x=1; x<=10; x++) {
            System.out.println(x);

        }
        
    }
    
}
//This loop initializes x to 1, checks if x is less than or equal to 10, prints the value of x, and then increments x by 1.